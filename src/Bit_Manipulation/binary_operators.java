package Bit_Manipulation;

public class binary_operators {
    public static void main(String[] args) {
        //bitwise and
        System.out.println(5 & 6);
        //bitwise or
        System.out.println(5 | 6);
        //bitwise xor
        System.out.println(5 ^ 6);
        //one's complement
        System.out.println(~5);
        //left shift
        System.out.println(5<<2);
        //right shift
        System.out.println(6>>1);
    }
}
